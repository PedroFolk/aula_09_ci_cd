def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def som(x, y):
    soma = x + y
    return soma

def sub(x, y):
    subtrair = x - y
    return subtrair

def mult(x, y):
    mult = x * y
    return mult

